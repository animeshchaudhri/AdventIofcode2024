#include <bits/stdc++.h>

using ll = long long;
using namespace std;
const ll mod = 1000000007;




void solve() {
    int ans = 0;
    for (int i = 0; i < 1000; i++) {
        vector<ll> v1;
        int flag = 0;
        int inc = 0, dec = 0;

        
        string line;
        getline(cin, line);
        stringstream ss(line);
        int n;

      
        while (ss >> n) {
            v1.push_back(n);
        }

       
        for (int i = 0; i < v1.size() - 1; i++) {
            int diff = v1[i + 1] - v1[i];
            
           
            if (abs(diff) < 1 || abs(diff) > 3) {
                flag = 1;
                break;
            }

            
            if (diff > 0) dec = 1;
            if (diff < 0) inc = 1;
        }

        if (flag == 0 && (inc || dec) && !(inc && dec)) {
            ans++;
        }
    }

    cout << ans << endl;
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    ll tc = 1;

    // cin >> tc;

    for (ll i = 1; i <= tc; ++i)
    {
        solve();
    }
}
